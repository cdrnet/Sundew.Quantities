namespace Sundew.Quantities.Formatters.MathML
{
    /// <summary>
    /// Enum for specifying a multiplication sign.
    /// </summary>
    public enum MultiplicationSign
    {
        /// <summary>
        /// The invisible multiplication sign.
        /// </summary>
        Invisible,

        /// <summary>
        /// The star multiplication sign (*).
        /// </summary>
        Star,

        /// <summary>
        /// The dot multiplication sign (·).
        /// </summary>
        Dot,

        /// <summary>
        /// The cross multiplication sign (×).
        /// </summary>
        Cross
    }
}